    public void fillEditor()
    {
    positionCaret(0);
    cm.execute(new GrammarToEditorCommand(notate.getGrammarFileName(), this));
    positionCaret(0);
    }
    
    public void positionCaret(int value)
      {
        sourceEditor.setCaretPosition(0);
      }
    
